#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
